    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        assentoDAO cadastroAdmDAO = new assentoDAO();
        cadastroadm = cadastroAdmDAO.consultar(1);
        
        assento ca;
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        for(int i=0; i<cadastroadm.size();i++){
            ca = (assento) cadastroadm.get(i);
            Vector linha = new Vector();
            linha.add(ca.getCod());
            linha.add(ca.getCodaviao());         
            modelo.addRow(linha);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

